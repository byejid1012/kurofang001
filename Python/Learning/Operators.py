#Type of Operators
# Arithmetic Operators
print(8**3)
print("-------------")

# Comparison Operators 
print (3>2)
print (3<2)
print("-------------")


# Assignment Operators
score = 5
score += 1
print(score)
score *=3
print(score)
print("-------------")

# Logical Operators
print(3>2 and 3<2)
print(3>2 or 3<2)
print(not(3>2))
print("-------------")

# Identity Operators
#is and is not
a = 543
b = 543
print(a is b)
print("-------------")
# Bitwise Operators
print(bin(15)) # Binary representation of 15
print(bin(8))  # Binary representation of 8
print(15 & 8) # Bitwise AND
print((15 | 8)) # Bitwise OR
print((15 ^ 8)) # Bitwise XOR
print((~15)) # Bitwise NOT
print((15 << 2)) # Left Shift
print((15 >> 2)) # Right Shift
print("-------------")


# Membership Operators
# in and not in
x = "Byejid Mondal"
print("Byejid" in x)
print("Mondal" not in x)
print("-------------")
